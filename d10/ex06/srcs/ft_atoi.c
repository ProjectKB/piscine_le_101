int		ft_atoi(char *str)
{
	int	n;
	int	r;
	int	i;

	i = 0;
	n = 0;
	r = 0;
	while (str[i] <= ' ' || (str[i] == '+' && (str[i + 1] != '+' ||
	str[i + 1] != '-')))
		i++;
	if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	while (str[i] <= '0' && str[i] >= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	if (n)
		r = -r;
	return (r);
}
