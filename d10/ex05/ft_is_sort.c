int		ft_is_sort(char *tab, int (*f)(int, int))
{
	int	i;

	i = 0;
	while (tab[i + 1])
	{
		if (f(tab[i], tab[i + 1]) != 1)
			return (0);
		i++;
	}
	return (1);
}
