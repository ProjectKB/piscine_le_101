#include "my.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_putstr2(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar2(str[i]);
		i++;
	}
}
